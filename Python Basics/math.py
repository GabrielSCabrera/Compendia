import math                     #Gives access to numerous functions

x = 1
n = 2

addition = x + n
subtraction = x - n

multiplication = x * n
division = x / n

exponentiation = x ** n
e_to_the_n = math.exp(n)
square_root = math.sqrt(x)

logarithm = math.log(x,n)       #Logarithm of x with base n
natural_logarithm = math.log(x) #Leaving out the n makes this a natural logarithm
log10 = math.log10(x)           #Logarithm base 10

modulo = x % n                  #Modulo function of x with respect to n

sine = math.sin(x)              #Trigonometric functions return and use radians
cosine = math.cos(x)
tangent = math.tan(x)

arcsine = math.asin(x)
arccosine = math.acos(x)
arctangent = math.atan(x)

degrees_to_radians = math.radians(x)
radians_to_degrees = math.degrees(x)

factorial = math.factorial(x)
gamma_function = math.gamma(x)

absolute_value = math.fabs(x)

floor_function = math.floor(x)  #Rounds x down to nearest integer
rounding =  round(x)            #Rounds x to nearest integer

pythagorean_theorem = math.hypot(x,n)

check_for_infinite_value = math.isinf(x)

pi = math.pi

e = math.e
